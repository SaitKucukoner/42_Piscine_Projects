void	ft_rev_int_tab(int *tab, int size)
{
	int	tmp;
	int	i;

	i = 0;
	while (i < size / 2)
	{
		tmp = tab[size - 1 - i];
		tab[size -1 - i] = tab[i];
		tab[i] = tmp;
		i++;
	}
}
